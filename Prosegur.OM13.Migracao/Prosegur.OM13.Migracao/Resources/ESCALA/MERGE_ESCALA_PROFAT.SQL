MERGE INTO dbo.fat_tcadesc ESC1
 USING (SELECT MAX(CODESC)+1 AS CODESC, 'PAR_CODESC' CODESC from fat_tcadesc) ESC2
 ON (ESC1.CODESC = ESC2.CODESC)
 WHEN MATCHED THEN 
	UPDATE SET DATULTALT = GETDATE(), CODUSU = 1100 , CODMARTE = 'PAR_CODMARTE'
 WHEN NOT MATCHED THEN
	INSERT (CODESC     , DESESC      , CODUSU, DATUTLALT, CODMARTE)
	VALUES (ESC2.CODESC, 'PAR_DESESC', 1100  , GETDATE(), 'PAR_CODMARTE');
