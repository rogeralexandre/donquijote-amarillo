UPDATE MARTE.COPR_TCLIENTE
   SET OID_SEGMENTO = (SELECT OID_SEGMENTO FROM MARTE.COPR_TSEGMENTO WHERE COD_SEGMENTO = 'COD_RAMO'),
  	   OID_SUBSEGMENTO = (SELECT SS.OID_SUBSEGMENTO 
                            FROM MARTE.COPR_TSUBSEGMENTO SS 
                           INNER JOIN MARTE.COPR_TSEGMENTO S ON S.OID_SEGMENTO = SS.OID_SEGMENTO
                           WHERE SS.COD_SUBSEGMENTO = 'COD_SUBRAMO'
                             AND S.COD_SEGMENTO = 'COD_RAMO'),
       DES_MUID = 'OM13',
       FYH_MDATE = SYSDATE
WHERE COD_CLIENTE = 'COD_PROFAT'
